#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tmp;
	size_t	slen;
	int		i;

	slen = ft_strlen((char *)s1);
	tmp = (char *)malloc(sizeof(char) * (slen + 1));
	if (!tmp)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		tmp[i] = s1[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
